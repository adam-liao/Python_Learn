# 8.	使用者輸入一組字串，檢查是否符合規則：1.至少8個字元，2.只有英文字母和數字，3.至少有一個大寫英文字母。符合顯示"密碼已設定"，不合顯示"密碼設定不符規則"


# 2025/04/24-19:15
pwd = input("請輸入密碼（需符合以下條件）：\n1. 至少8個字元\n2. 僅限英文字母和數字\n3. 至少一個大寫字母\n> ")

if len(pwd) >= 8 and pwd.isalnum() and any(c.isupper() for c in pwd):
    print("✅ 密碼已設定")
else:
    print("❌ 密碼設定不符規則")
